# 5.1 варіант 13. Дана степінь натурального числа A. Знайти її показник.
try:
    a = int(input('Введіть число:'))
    n = int(input('Введіть степінь:'))
    b = a
    i = 2

    while i <= n:
        b = b*a
        i = i+1

    print('Показник', a, 'в степені', n, '=', b)
except:
    print('Перевірте правельність написання числа та степеню')